const os = require('os');

//Get platform
console.log(os.platform());

//Get OS Architecture
console.log(os.arch());

//Get CPU INFO
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//Total Memory
console.log(os.totalmem());

//Home Directory
console.log(os.homedir());

//Up time of the system
console.log(os.uptime());